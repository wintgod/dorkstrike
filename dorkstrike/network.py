class TokenBucket:
    """Simple token-bucket rate limiter (thread-safe)."""

    def __init__(self, rate: int):
        """
        Args:
            rate: Maximum tokens (requests) per minute.
        """
        self.rate = rate
        self.tokens = float(rate)
        self.max_tokens = float(rate)
        self.last_refill = time.monotonic()
        self._lock = threading.Lock()

    def acquire(self) -> None:
        """Block until a token is available."""
        while True:
            with self._lock:
                now = time.monotonic()
                elapsed = now - self.last_refill
                self.tokens = min(self.max_tokens, self.tokens + elapsed * (self.rate / 60.0))
                self.last_refill = now

                if self.tokens >= 1.0:
                    self.tokens -= 1.0
                    return

            # No token available — wait a bit
            time.sleep(0.1)


class ProxyRotator:
    """Round-robin proxy rotation (thread-safe)."""

    def __init__(self, proxies: list[str]):
        self._proxies = proxies
        self._index = 0
        self._lock = threading.Lock()

    def next(self) -> str:
        """Return the next proxy URL."""
        with self._lock:
            proxy = self._proxies[self._index % len(self._proxies)]
            self._index += 1
            return proxy


def create_session(
    timeout: int = 10,
    proxy: Optional[str] = None,
) -> requests.Session:
    """Create a requests.Session with retry logic, random UA, and optional proxy.

    Args:
        timeout: Default request timeout.
        proxy: Optional proxy URL.

    Returns:
        Configured session.
    """
    session = requests.Session()

    # Retry with backoff on transient *server* errors only.
    # 429 (rate-limit) is NOT retried here — each engine handles it
    # explicitly so it can log and stop gracefully instead of looping.
    retry = Retry(
        total=2,
        backoff_factor=1.0,
        status_forcelist=[500, 502, 503, 504],
        allowed_methods=["GET", "POST"],
    )
    adapter = HTTPAdapter(max_retries=retry)
    session.mount("https://", adapter)
    session.mount("http://", adapter)

    # Random User-Agent
    session.headers.update({
        "User-Agent": random.choice(USER_AGENTS),
        "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8",
        "Accept-Language": "en-US,en;q=0.9",
        "Accept-Encoding": "gzip, deflate",
        "DNT": "1",
        "Connection": "keep-alive",
        "Upgrade-Insecure-Requests": "1",
    })

    # Proxy
    if proxy:
        session.proxies = {"http": proxy, "https": proxy}

    return session


def apply_delay(delay: float) -> None:
    """Sleep for delay + random jitter (0-1s) to appear human-like."""
    jitter = random.uniform(0.0, 1.0)
    total = delay + jitter
    logger.debug("Sleeping %.2fs (delay=%.2f + jitter=%.2f)", total, delay, jitter)
    time.sleep(total)
